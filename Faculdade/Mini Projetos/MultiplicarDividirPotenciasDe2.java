public class MultiplicarDividirPotenciasDe2 {
    public static void main(String[] args) {
        int n = 6;

        // Multiplicação por potências de 2
        System.out.println(n << 1); // 6 * 2 = 12
        System.out.println(n << 2); // 6 * 4 = 24

        // Divisão por potências de 2
        System.out.println(n >> 1); // 6 / 2 = 3
        System.out.println(n >> 2); // 6 / 4 = 1
    }
}
